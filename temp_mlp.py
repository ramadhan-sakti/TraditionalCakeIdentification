from sklearn.neural_network import MLPClassifier

# =============================================================================
# Inisialisasi
# =============================================================================

x = [[0.,0.],   # x = Data Training
     [1.,1.]
    ]

y = [0,1]       # y = Nilai Target

classifier = MLPClassifier(solver='sgd', alpha=1e-5,
                           hidden_layer_sizes=(5,2),random_state=1)

# =============================================================================
# Training
# =============================================================================
classifier.fit(x,y)

# =============================================================================
# Test
# =============================================================================
classifier.predict([[2.,2.], [-1.,-2.]])